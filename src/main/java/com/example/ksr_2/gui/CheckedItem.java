package com.example.ksr_2.gui;

public class CheckedItem {
    private String category;
    private String item;

    public CheckedItem(String category, String item) {
        this.category = category;
        this.item = item;
    }

    public String getCategory() {
        return category;
    }

    public String getItem() {
        return item;
    }

}
